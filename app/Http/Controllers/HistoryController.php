    public function getHistory(){
         $status = DB::select(
            "
             SELECT products.id, histories.type, products.product_name, histories.quantity, suppliers.name, histories.created_at
             FROM products
             JOIN histories ON products.id = histories.product_id
             JOIN suppliers ON products.supplier_id = suppliers.id;
            "
        );

        return view('history', compact('status'));
    }

